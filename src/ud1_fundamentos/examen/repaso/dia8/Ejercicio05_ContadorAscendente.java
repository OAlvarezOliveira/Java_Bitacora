/*
Ejercicio 1 — Contador ascendente

Muestra los números del 1 al 100 en la misma línea separados por espacio.  
  
  
 */

public class Ejercicio05_ContadorAscendente {
	
public static void main(String[] args) {
	
	//Operativa
	for (int i = 1 ; i<=100 ; i++) {
		//Salida de datos
		System.out.print(i+" ");
	}
}

}
